package com.spring.basic.hellospringbasic01.service;

public class Post{
	private int id;
	private String title;
	private String body;
	private int userId;

	public int getId(){
		return id;
	}

	public String getTitle(){
		return title;
	}

	public String getBody(){
		return body;
	}

	public int getUserId(){
		return userId;
	}
}
